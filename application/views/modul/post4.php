                    <small>
                        Teknologi merupakan hasil dari perkembangan ilmu pengetahuan dan teknologi yang mampu membantu memudahkan pekerjaan manusia dalam berbagai bidang, salah satunya pertanian. Teknologi pertanian adalah cara atau metode yang digunakan dalam mengolah atau memproses input pertanian sehingga dapat menghasilkan output pertanian sehingga berdaya guna baik berupa bahan mentah maupun produk bahan mentah. Teknologi memegang peran penting dalam pengembangan potensi sumberdaya tanaman pangan, sumberdaya peternakan, sumberdaya kehutanan, sumberdaya perikanan. Di daerah kelurahan Tlogo Patut, Gresik memiliki keterbatasan ketersediaan lahan pertanian, terutama di daerah urban serta kebutuhan akan pangan yang mendesak sehingga untuk menyikapinya masyarakat di daerah tersebut bertanam di sekitar rumah dengan menggunakan teknik vertikultur yang model dan jenis tanaman yang ditanam menyesuaikan dengan kondisi setempat. sistem vertikultur di Tlogo Patut yaitu dengan menggunakan botol bekas dan pipa. Namun teknik vertikultur sebenarnya dapat dilakukan menggunakan berbagai macam wadah (tempat media tanam) seperti pipa paralon, botol bekas, pot, polybag atau wadah lainnya tergantung kreatifitas masing-masing. Dengan menggunakan barang-barang bekas berperan aktif untuk meningkatkan nilai tambah barang bekas serta mengurangi pencemaran lingkungan oleh penumpukan sampah-sampah tersebut. Yang terpenting, bahan untuk vertikultur bersifat kuat dan fleksibel untuk dipindah tempatkan. Ada beberapa syarat utama dalam penanaman dengan teknik vertikultur diantaranya adalah wadah penanaman vertikultur harus bersifat kuat, mudah dipindahkan dan tidak mudah roboh. Ukuran wadah tanaman disesuaikan dengan jenis tanaman. Misalnya, gunakan pipa paralon dengan diameter 3 inchi atau botol bekas untuk menanam sayuran seperti bayam, sawi, caisim, kangkung. Sedangkan untuk tanaman seperti terong, cabai, tomat, gunakan wadah lebih besar. Tempat vertikultur harus berada di luar ruangan yang terkena matahari cukup. Media tanam harus bersifat gembur serta memiliki unsur hara yang cukup. Seiring berkembangnya teknologi pertanian, inovasi urban farming sangat dibutuhkan yaitu seperti budidaya Hidroponik. Hidroponik adalah suatu metode bercocok tanam tanpa menggunakan media tanah, melainkan dengan
                        menggunakan larutan mineral bernutrisi atau bahan lainnya yang mengandung unsur hara seperti sabut kelapa, serat mineral, pasir, pecahan batu bata, serbuk kayu, dan lain-lain sebagai pengganti media tanah (Izzuddin, 2016). Tanah yang sejatinya merupakan tempat tumbuhnya tanaman dapat digantikan dengan media inert, seperti pasir, arang sekam, rockwool, kapas, kerikil, dll. Di daerah dengan lahan yang tidak produktif/margin, hidroponik menawarkan kegiatan pertanian yang dapat dikembangkan dengan baik. Sistem hidroponik merupakan cara produksi tanaman yang sangat efektif. Sistem ini dikembangkan berdasarkan alasan bahwa jika tanaman diberi kondisi pertumbuhan yang optimal, maka potensi maksimum untuk berproduksi dapat tercapai. Hal ini berhubungan dengan pertumbuhan sistem perakaran tanaman, di mana pertumbuhan perakaran tanaman yang optimum akan menghasilkan pertumbuhan tunas atau bagian atas yang sangat tinggi. Pada sistem hidroponik, larutan nutrisi yang diberikan mengandung komposisi garam-garam organik yang berimbang untuk menumbuhkan perakaran dengan kondisi lingkungan perakaran yang ideal. Sistem hidroponik vertikultur dengan menggunakan barang bekas seperti galon dan botol bekas merupakan salah satu sistem hidroponik yang memanfaatkan barang bekas dengan begitu mampu mengurangi sampah plastik. Sistem hidroponik yang akan digunakan yaitu menggunakan wick sistem (sumbu). Wick system memanfaatkan daya kapilaritas agar air mengandung nutrisi naik melalui sumbu atau kain dan mengenai akar, sehingga nutrisi dapat diserap oleh akar, sumbu yang digunakan umumnya adalah kain flanel atau jenis bahan lain yang mudah menyerap air. Sementara itu, larutan nutrisi ditempatkan pada wadah berupa bak plastik atau botol plastik. Jadi, akar tidak langsung bersentuhan dengan nutrisi tetapi melalui perantara sumbu dan kain. Wick Sistem ini menggunakan botol bekas namun pada proses penataanya akan menggunakan model vertikultur sehingga dikatakan dengan istilah hidroponik vertikultur. Program penyuluhan hidroponik dan vertikultur dilakukan secara virtual dengan memberikan materi tentang cara budidaya hidroponik dan vertikultur sampai dengan panen. Materi yang akan disosialisasikan dibuat dalam bentuk power point, yang kemudian akan diberikan kepada warga kelurahan Tlogo Patut, sehingga dengan adanya materi tersebut warga akan lebih mudah dalam melakukan budidaya hidroponik dan vertikultur.
